package Day2;

public class TypeCasting {
    public static void main(String[] args) {

        byte e = 20;
        int s = e;
        System.out.println(s);


        int s1 = 100;
        byte e1 = (byte) s1;
        System.out.println(e1);

        int a = 5;
        double b = 25.0;
        System.out.println(b/a);
        System.out.println((int)b/a);

        System.out.println((char) 66);
        System.out.println((int) 'c');





    }
}
